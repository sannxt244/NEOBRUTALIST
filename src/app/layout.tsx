import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";
import Link from "next/link";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

enum ConversationType {
  Personal = 1,
  Group = 2,
}

interface IConversation {
  Id: string;
  Type: ConversationType;
  Name: string;
  Users: {
    FullName: string;
  }[];
}

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const data = await fetch(
    "https://misajsc.amis.vn/chat/api/business/v1/conversations/me?TagIds=",
    {
      headers: {
        accept: "application/json, text/plain, */*",
        "accept-language": "en-US,en;q=0.9,vi;q=0.8",
        "access-control-allow-origin": "*",
        devicename: "Web",
        "response-type": "json",
        "sec-ch-ua":
          '"Google Chrome";v="137", "Chromium";v="137", "Not/A)Brand";v="24"',
        "sec-ch-ua-mobile": "?0",
        "sec-ch-ua-platform": '"Windows"',
        "sec-fetch-dest": "empty",
        "sec-fetch-mode": "cors",
        "sec-fetch-site": "same-origin",
        cookie: process.env.NEXT_CHAT_COOKIE,
      },
      referrer: "https://misajsc.amis.vn/chat/cv/67db92ff474bf706bf58b5ba",
      referrerPolicy: "strict-origin-when-cross-origin",
      body: null,
      method: "GET",
      mode: "cors",
      credentials: "include",
    },
  );
  const conversations = await data.json();

  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        <div className="h-screen overflow-hidden bg-gradient-to-br from-purple-50 to-blue-50 p-2 sm:p-4 md:p-8">
          <div className="mx-auto flex h-full w-full max-w-7xl flex-col rounded-3xl border-4 border-black bg-white/30 shadow-[8px_8px_0px_0px_rgba(0,0,0,1)] backdrop-blur-xl">
            <header className="border-b-4 border-black bg-white/40 p-4 backdrop-blur-md sm:p-6">
              <div className="flex items-center justify-between gap-4">
                <h1 className="text-2xl font-black tracking-tight sm:text-3xl md:text-4xl">
                  L
                </h1>

                {/* Mobile menu */}
                <div className="flex md:hidden"></div>

                {/* Desktop buttons */}
                <div className="hidden items-center gap-3 sm:flex">
                  <button className="btn rounded-xl border-2 border-black bg-black font-bold text-white shadow-[4px_4px_0px_0px_rgba(0,0,0,1)] hover:bg-black/80">
                    Sign in
                  </button>
                  <button className="btn rounded-xl border-2 border-black font-bold shadow-[4px_4px_0px_0px_rgba(0,0,0,1)]">
                    Settings
                  </button>
                </div>
              </div>
            </header>
            <div className="grid h-[calc(100vh-6rem)] min-h-0 flex-1 overflow-hidden md:grid-cols-[280px_1fr]">
              <div className="hidden overflow-y-auto border-r-4 border-black bg-white/40 md:block">
                <nav className="space-y-2">
                  {conversations.map((conversation: IConversation) => (
                    <Link
                      key={conversation.Id}
                      href={`/conversations/${conversation.Id}`}
                    >
                      <button className="flex items-center gap-2 rounded-xl p-3">
                        {conversation.Type === ConversationType.Group
                          ? conversation.Name
                          : conversation.Users[0]?.FullName}
                      </button>
                    </Link>
                  ))}
                </nav>
              </div>
              {children}
            </div>
          </div>
        </div>
      </body>
    </html>
  );
}
